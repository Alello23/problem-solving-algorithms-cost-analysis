

public class Sailor {
    public int id;
    public String name;
    public float weight;
    public int[] abilities;
    public int WL;

    public Sailor(int id, String name, float weight, int[] abilities, int WL){
        this.id = id;
        this.name = name;
        this.weight = weight;
        this.abilities = abilities;
        this.WL = WL;
    }

}
